package proyecto2;

public class Paciente {
	int cedula;
	String nombre;
	String sintomas[];
	float valorTratamiento;
	
	public Paciente(int cedula, String nombre, String[] sintomas, float valorTratamiento) {
		this.cedula = cedula;
		this.nombre = nombre;
		this.sintomas = sintomas;
		this.valorTratamiento = valorTratamiento;
	}
	
	
	

}
